--Card Effects--START
--While you have 4 or more WATER or Winged Beast monsters in your Graveyard, this card cannot be destroyed by card effects.
function s.indfilter(c)
	return c:IsAttribute(ATTRIBUTE_WATER) or c:IsRace(RACE_WINGEDBEAST)
end

function s.indcon(e,tp,eg,ep,ev,re,r,rp)
	return Duel.IsExistingMatchingCard(s.indfilter,e:GetHandlerPlayer(),LOCATION_GRAVE,0,4,nil)
end

--You can discard 1 WATER or Winged Beast monster
function s.cfilter(c)
	return (c:IsAttribute(ATTRIBUTE_WATER) or c:IsRace(RACE_WINGEDBEAST)) and c:IsDiscardable() and c:IsAbleToGraveAsCost()
end
function s.tdcost(e,tp,eg,ep,ev,re,r,rp,chk)
	if chk==0 then return Duel.IsExistingMatchingCard(s.cfilter,tp,LOCATION_HAND,0,1,nil) end
	Duel.DiscardHand(tp,s.cfilter,1,1,REASON_COST+REASON_DISCARD)
end
function s.tdfilter(c)
	return c:IsAbleToDeck()
end

--Target 1 card on the field
function s.tdtg(e,tp,eg,ep,ev,re,r,rp,chk,chkc)
	if chkc then return chkc:IsOnField() and s.tdfilter(chkc) and chkc~=e:GetHandler() end
	if chk==0 then return Duel.IsExistingTarget(s.tdfilter,tp,LOCATION_ONFIELD,LOCATION_ONFIELD,1,nil) end
	Duel.Hint(HINT_SELECTMSG,tp,HINTMSG_TODECK)
	local g=Duel.SelectTarget(tp,s.tdfilter,tp,LOCATION_ONFIELD,LOCATION_ONFIELD,1,1,nil)
	Duel.SetOperationInfo(0,CATEGORY_TODECK,g,1,0,0)
end

--Shuffle it into the Deck
function s.tdop(e,tp,eg,ep,ev,re,r,rp)
	local tc=Duel.GetFirstTarget()
	if tc:IsRelateToEffect(e) then
		Duel.SendtoDeck(tc,nil,2,REASON_EFFECT)
	end
end


