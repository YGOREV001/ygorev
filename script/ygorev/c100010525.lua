--Card Effects--START
--If this card inflicts battle damage to your opponent
function s.thcon(e,tp,eg,ep,ev,re,r,rp)
	return ep~=tp
end
--You can target 1 Level 3 or lower Normal Monster in your Graveyard
function s.filter(c,tp)
	return c:IsLevelBelow(3) and c:IsType(TYPE_NORMAL) and Duel.IsExistingMatchingCard(s.nfilter1,tp,LOCATION_DECK,0,1,nil,c)
end
function s.nfilter1(c,tc)
	return c:IsCode(tc:GetCode()) and c:IsAbleToHand()
end
function s.nfilter2(c,tc)
	return c:IsCode(tc:GetPreviousCodeOnField()) and c:IsAbleToHand()
end
function s.thtg(e,tp,eg,ep,ev,re,r,rp,chk,chkc)	
	if chkc then return chkc:IsLocation(LOCATION_GRAVE) and s.filter(chkc,e,tp) end
	if chk==0 then return Duel.IsExistingTarget(s.filter,tp,LOCATION_GRAVE,0,1,nil,tp) end
	Duel.Hint(HINT_SELECTMSG,tp,HINTMSG_TARGET)
	local g=Duel.SelectTarget(tp,s.filter,tp,LOCATION_GRAVE,0,1,1,nil,tp)
	Duel.SetOperationInfo(0,CATEGORY_TOHAND,nil,1,tp,LOCATION_DECK)
end
--Add 1 monster with the same name as that target from your Deck to your hand
function s.thop(e,tp,eg,ep,ev,re,r,rp)
	local tc=Duel.GetFirstTarget()
	if tc:IsLocation(LOCATION_GRAVE) and tc:IsRelateToEffect(e) then
		Duel.BreakEffect()
		local g=Duel.SelectMatchingCard(tp,s.nfilter2,tp,LOCATION_DECK,0,1,1,nil,tc)
		local hc=g:GetFirst()
		if hc then
			Duel.SendtoHand(hc,nil,REASON_EFFECT)
			Duel.ConfirmCards(1-tp,hc)
		end
	end
end
--Gains 500 ATK for each "Skull Servant" and each FIRE monster in your Graveyard
function s.atkfilter(c,e,tp)
	return c:IsCode(YGOREV_CARD_SKULLSERVANT) or c:IsRace(RACE_WARRIOR)
end
function s.atkval(e,c)
	return Duel.GetMatchingGroupCount(s.atkfilter,e:GetHandlerPlayer(),LOCATION_GRAVE,0,nil)*500
end
