--Card Effects--START

--While this card is in Attack Position, it cannot be destroyed by battle. While this card is in Defense Position, it cannot be destroyed by card effects
function s.indbatcond(e)
	return e:GetHandler():IsAttackPos()
end
function s.indeffcond(e)
	return e:GetHandler():IsDefensePos()
end
--When a card or effect is activated: You can pay 1000LP; change this card's battle position
function s.cpcon(e,tp,eg,ep,ev,re,r,rp)
	return rp~=tp
end
function s.cpcost(e,tp,eg,ep,ev,re,r,rp,chk)
	if chk==0 then return Duel.CheckLPCost(tp,1000)
	else Duel.PayLPCost(tp,1000) end
end
function s.cpop(e,tp,eg,ep,ev,re,r,rp)
	local c=e:GetHandler()
	if c:IsRelateToEffect(e) then 	
		Duel.ChangePosition(c,POS_FACEUP_DEFENSE,POS_FACEUP_DEFENSE,POS_FACEUP_ATTACK,POS_FACEUP_ATTACK)
	end
end
